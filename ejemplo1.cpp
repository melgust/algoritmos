//Aplicion que suma dos numeros ingresados por el usuario
#include <iostream>

using namespace std;

int main() {
    float var1;
    float var2;
    cout << "Ingrese numero: ";
    cin >> var1;
    cout << "Ingrese numero 2: ";
    cin >> var2;
    cout << "La suma es: " << var1 + var2;
    cout << endl;
    cout << "\n";
    return 0;
}